/**
 * Copy text to the clipboard, resilient to environments where the async
 * Clipboard API is unavailable or rejects (e.g. Electron loading over file://,
 * which isn't a secure context). Falls back to a hidden-textarea execCommand.
 */
export async function copyText(text: string): Promise<boolean> {
  try {
    await navigator.clipboard.writeText(text);
    return true;
  } catch {
    try {
      const ta = document.createElement("textarea");
      ta.value = text;
      ta.style.position = "fixed";
      ta.style.opacity = "0";
      document.body.appendChild(ta);
      ta.select();
      const ok = document.execCommand("copy");
      document.body.removeChild(ta);
      return ok;
    } catch {
      return false;
    }
  }
}
